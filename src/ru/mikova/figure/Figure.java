package ru.mikova.figure;

import ru.mikova.point.Point;

/**
 * Абстрактный базовый класс для геометрических фигур.
 * Задание 4.4 Фигуры.
 * Необходимо продумать структуру и организацию следующих геометрических фигур:
 * 1. Круг. Задается точкой координат центра и радиусом.
 * 2. Квадрат. Рекомендуется взять квадрат полученный в задаче 2.1.3
 * 3. Прямоугольник. Задается точкой координат левого верхнего угла и двумя сторонами.
 * 4. Треугольник. Задается тремя точками координат.
 * Для каждой фигуры обязательно должна быть возможность рассчитать её площадь
 */
public abstract class Figure implements IShape {
    private Point location;
    private String name;

    /**
     * Создает новую фигуру с указанным местоположением и именем.
     * @param location точка привязки фигуры
     * @param name название фигуры
     */
    protected Figure(Point location, String name) {
        this.location = location;
        this.name = name;
    }

    /**
     * Возвращает строковое представление фигуры.
     * @return строка с описанием фигуры
     */
    @Override
    public String toString() {
        return "Фигура: " + name + ", Позиция: " + location;
    }
}